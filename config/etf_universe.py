def extract_index_name(name: str) -> str | None:
    """从 ETF 名称中提取跟踪指数名。

    例如 "华泰柏瑞沪深300ETF" → "沪深300"
         "华夏上证科创板50成份ETF" → "上证科创板50成份"
    """
    if pd.isna(name):
        return None
    name = name.strip()
    # 去后缀：ETF、LOF、QDII
    name = re.sub(r'(ETF|LOF|QDII)(\(QDII\))?$', '', name)
    # 去分级基金后缀
    name = re.sub(r'[ABC]$', '', name)
    name = re.sub(r'-.+$', '', name)
    # 去基金公司前缀
    name = _COMPANY_RE.sub('', name)
    # 去连接词
    name = re.sub(r'^(中证|上证|深证|标普|恒生|纳斯达克|MSCI|国证|富时)', r'\1', name)
    return name.strip() if name else None


